    public void charger_tab_tranche(JTable tab)
    {
        ResultSet resul=Examen.Connect.extraireData("select *from tranche");
        int index,i=0;
        index=Examen.Generalites.getResultSetRowCount(resul);
        String[] titres={"trID","Nom du tranche"};
        String[][]data=new String[index][titres.length];
        if(resul!=null)
        {
            try {
                while(resul.next())
                {
                    data[i][0]=""+resul.getInt("trId");
                    data[i][1]=resul.getString("nom_tra");
                    i++;
                }
                tab.setModel(new Model_table(data,titres));
            } catch (SQLException ex) {
                Logger.getLogger(UI_tranche.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
    }
    public void deplace_tranche()
    {
        tab_tranche.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
            @Override
         
            public void valueChanged(ListSelectionEvent e) {
             bt_enregistre.setEnabled(false);
               bt_supprime.setEnabled(true);
                int index_line=-1;
                index_line=tab_tranche.getSelectedRow();
                if(index_line==-1)
                    return;
                txt_tranche.setForeground(new Color(0,0,0));
                txt_tranche.setText(tab_tranche.getValueAt(index_line,1).toString());
                if(tr==null)
                    tr=new Tranches();
                tr.setTrId(Integer.parseInt(tab_tranche.getValueAt(index_line, 0).toString()));
            }
            
        });
    }

