    private void bt_enregistreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_enregistreActionPerformed
        // TODO add your handling code here:
        if(txt_tranche.getText().trim().equalsIgnoreCase("")||txt_tranche.getText().trim().equalsIgnoreCase("Tapez le nom du tranche ici"))
        {
            Examen.Generalites.Donner_info_User("Saisissez d'abord le nom de la tranche");
            return;
        }
        else
        {
            tr=new Tranches();
            tr.setNom(txt_tranche.getText());
            if(tr.enregistre_tranche()>0)
            {
                
                Examen.Generalites.Donner_info_User("L'enregistrement d'une tranche est reussie!");
                charger_tab_tranche(tab_tranche);
                txt_tranche.setForeground(new Color(102,102,102));
                txt_tranche.setText("Tapez le nom du tranche ici");
            }
            else
                Examen.Generalites.Donner_info_User("Echec de l'enregistrement!");
                txt_tranche.setForeground(new Color(102,102,102));
                txt_tranche.setText("Tapez le nom du tranche ici");
            
             bt_enregistre.setEnabled(true);
             bt_supprime.setEnabled(false);


        }
    }//GEN-LAST:event_bt_enregistreActionPerformed

    private void bt_supprimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_supprimeActionPerformed
        // TODO add your handling code here:
        if(txt_tranche.getText().trim().equalsIgnoreCase("")||txt_tranche.getText().trim().equalsIgnoreCase("Tapez le nom du tranche ici"))
        {
            Examen.Generalites.Donner_info_User("Selectionner dans le tableau la tranche à modifier");
            return;
        }
        else{
        if(tr.supprime_tranche()>0)
        {
            

            Examen.Generalites.Donner_info_User("la suppression d'une tranche est effectuee!");
            charger_tab_tranche(tab_tranche);
            txt_tranche.setForeground(new Color(102,102,102));
            txt_tranche.setText("Tapez le nom du tranche ici");
        }
        else
            Examen.Generalites.Donner_info_User("Ëchec de la suppression");
        
          
         bt_enregistre.setEnabled(true);
         bt_supprime.setEnabled(false);
        }
    }//GEN-LAST:event_bt_supprimeActionPerformed

